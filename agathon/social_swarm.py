            template_id=f"VISH-{uuid4().hex[:6]}",
            category="vishing_script",
            platform=Platform.EMAIL,
            title="MFA Fatigue / Helpdesk Impersonation",
            target_role=EmployeeRole.IT_HELPDESK,
            urgency=UrgencyLevel.CRITICAL,
            scenario_context="Exploits 'MFA Fatigue' and the helpful nature of support staff.",
