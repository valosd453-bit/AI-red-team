class AuthorisationState(Enum):
    """Current authorisation state of the module."""
    LOCKED     = auto()   # No token provided — module refuses to emit content
    VALIDATED  = auto()   # Token accepted — module operational
    EXPIRED    = auto()   # Token was valid but has timed out
    INVALID    = auto()   # Token failed verification

@dataclass(frozen=True)
class EngagementToken:
    """
    Immutable engagement authorisation token.
    Generated by ForgeGuard's admin panel and signed with the organisation's
    private key. This token pins output to a specific approved engagement.
    """
    engagement_id: str
    organisation_id: str
    authorised_by: str           # Identity of the approving security lead
    scope_of_work: str           # e.g. "Q3 2026 Internal Red Team — Developer Phishing"
    target_domain: str           # The domain owned & verified via Target Verification Protocol
    issued_at: str               # ISO 8601
    expires_at: str              # ISO 8601
    signature: str               # HMAC-SHA256(engagement_id|org_id|expires_at, org_secret)

    def is_expired(self) -> bool:
        expire = datetime.fromisoformat(self.expires_at.replace("Z", "+00:00"))
        return datetime.now(timezone.utc) > expire

    def to_watermark(self) -> str:
        """Produces a compact watermark embedded in every generated template."""
        return (
            f"FORGEGUARD-TRACE:{self.engagement_id}:{self.organisation_id}:"
            f"{self.authorised_by}:{self.target_domain}"
        )

class AuthorisationGuard:
    """
    Enforces that the module cannot produce social-engineering content
    without a valid, unexpired engagement token signed by the organisation.
    """

    def __init__(self, organisation_secret: Optional[str] = None):
        self._organisation_secret = organisation_secret or os.environ.get(
            "FORGEGUARD_ORG_SECRET", ""
        )
        self._current_token: Optional[EngagementToken] = None
        self._state = AuthorisationState.LOCKED

    # ── Token Verification ──

    def verify_and_activate(self, token_dict: Dict[str, str]) -> AuthorisationState:
        """Verify a token dict (as received from the ForgeGuard admin API) and activate if valid."""
        if not self._organisation_secret:
            self._state = AuthorisationState.LOCKED
            return self._state

        try:
            token = EngagementToken(**token_dict)
        except TypeError:
            self._state = AuthorisationState.INVALID
            return self._state

        # Check expiry
        if token.is_expired():
            self._state = AuthorisationState.EXPIRED
            return self._state

        # Verify signature
        payload = f"{token.engagement_id}|{token.organisation_id}|{token.expires_at}"
        expected = hmac.new(
            self._organisation_secret.encode(),
            payload.encode(),
            hashlib.sha256,
        ).hexdigest()

        if not hmac.compare_digest(expected, token.signature):
            self._state = AuthorisationState.INVALID
            return self._state

        self._current_token = token
        self._state = AuthorisationState.VALIDATED
        return self._state

    @property
    def is_authorised(self) -> bool:
        return self._state == AuthorisationState.VALIDATED

    @property
    def watermark(self) -> str:
        if not self._current_token:
            return "FORGEGUARD-TRACE:UNAUTHORISED"
        return self._current_token.to_watermark()

    def guard(self, fn: Callable) -> Callable:
        """Decorator that blocks execution unless authorised."""
        def wrapper(*args, **kwargs):
            if not self.is_authorised:
                raise PermissionError(
                    "Social Swarm is LOCKED. Provide a valid ForgeGuard Engagement Token "
                    "via verify_and_activate() before generating any simulation content. "
                    "This module is for AUTHORISED security training only."
                )
            result = fn(*args, **kwargs)
            # Embed watermark in string results
            if isinstance(result, str):
                result = (
                    f"{'─'*72}\n"
                    f"FORGEGUARD SOCIAL SWARM — AUTHORISED SIMULATION CONTENT\n"
                    f"Engagement: {self._current_token.engagement_id}\n"
                    f"Authorised by: {self._current_token.authorised_by}\n"
                    f"Target domain: {self._current_token.target_domain}\n"
                    f"Watermark: {self.watermark}\n"
                    f"Generated: {datetime.now(timezone.utc).isoformat()}\n"
                    f"THIS CONTENT IS FOR AUTHORISED SECURITY TRAINING ONLY.\n"
                    f"{'─'*72}\n\n"
                ) + result
            return result
        return wrapper


