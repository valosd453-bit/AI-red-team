class SocialSwarm:
    def __init__(self, guard: AuthorisationGuard):
        self.guard = guard
        self.builder = PhishingEmailBuilder()

    @AuthorisationGuard().guard
    def generate_exposure_map(self, meta: CompanyMetadata) -> SocialExposureMap:
        impersonation = OSINTContextAnalyser.derive_impersonation_targets(meta)[0]
        
        templates = [
            self.builder.build_1_api_key_audit(meta, self.guard.watermark, impersonation),
            self.builder.build_2_it_helpdesk_vishing(meta, self.guard.watermark)
        ]

        return SocialExposureMap(
            engagement_id=self.guard._current_token.engagement_id,
            organisation_id=self.guard._current_token.organisation_id,
            target_company=meta,
            generated_at=datetime.now(timezone.utc).isoformat(),
            expires_at=(datetime.now(timezone.utc) + timedelta(days=7)).isoformat(),
            templates=templates,
            summary_statistics={"total_vectors": len(templates), "critical_urgency": 1},
            training_deployment_guide="Deploy via internal phishing simulation tool. Track click-through rates.",
            disclaimer="FOR AUTHORISED TRAINING ONLY."
        )