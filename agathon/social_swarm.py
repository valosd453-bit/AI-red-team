class OSINTContextAnalyser:
    """
    Analyses CompanyMetadata to identify plausible pretexts, impersonation
    targets, and contextual hooks that make social-engineering scenarios
    believable — so the training is realistic and effective.
    """

    @staticmethod
    def derive_impersonation_targets(meta: CompanyMetadata) -> List[Dict[str, str]]:
        """
        Identify which real people/roles an attacker would impersonate
        based on public information. Used to make phishing scenarios realistic.
        """
        targets: List[Dict[str, str]] = []

        # From known employees
        for emp in meta.key_employees_public:
            if any(r in emp.get("role", "").lower() for r in
                   ["cto", "vp engineering", "head of engineering", "ceo", "founder"]):
                targets.append({
                    "name": emp.get("name", "Unknown"),
                    "role": emp.get("role", "Leadership"),
                    "source": "public_linkedin",
                    "impersonation_value": "high",
                })

        # Fallback: construct plausible authority figures
        if not targets:
            targets.append({
                "name": f"CTO @ {meta.company_name}",
                "role": "Chief Technology Officer",
                "source": "inferred_from_job_board",
                "impersonation_value": "high",
            })

        return targets

    @staticmethod
    def derive_tech_pretexts(meta: CompanyMetadata) -> List[str]:
        """Build a list of plausible technical pretexts based on the public tech stack."""
        pretexts: List[str] = []

        if any("openai" in t.lower() or "gpt" in t.lower() for t in meta.tech_stack_public):
            pretexts.append("OpenAI API key rotation / billing issue")
        if any("aws" in t.lower() for t in meta.tech_stack_public):
            pretexts.append("AWS IAM credential expiry / CloudTrail alert")
        if any("github" in t.lower() for t in meta.tech_stack_public):
            pretexts.append("GitHub PAT (Personal Access Token) audit")
        if any("docker" in t.lower() or "kubernetes" in t.lower() for t in meta.tech_stack_public):
            pretexts.append("Docker Hub / container registry credential refresh")
        if any("auth0" in t.lower() or "okta" in t.lower() or "clerk" in t.lower()
               for t in [meta.authentication_provider] + meta.tech_stack_public):
            pretexts.append(f"{meta.authentication_provider} SSO certificate expiry")

        if not pretexts:
            pretexts.append("CI/CD pipeline secret rotation")
            pretexts.append("Cloud provider API key audit")

        return pretexts

    @staticmethod
    def derive_temporal_hooks(meta: CompanyMetadata) -> List[str]:
        """Find time-sensitive hooks that add urgency to scenarios."""
        hooks: List[str] = []
        now = datetime.now(timezone.utc)

        # Conference season
        if meta.conference_presence:
            hooks.append(
                f"Upcoming conference presence: {meta.conference_presence[0]} — "
                f"attackers often target speakers/sponsors pre-conference"
            )

        # News-driven
        for news in meta.recent_news[:3]:
            if "funding" in news.lower() or "series" in news.lower():
                hooks.append(f"Recent funding announcement ({news[:80]}...) — heightened targeting risk")
            if "launch" in news.lower() or "release" in news.lower():
                hooks.append(f"Recent product launch ({news[:80]}...) — support-team impersonation vector")

        # Default seasonal hooks
        month = now.month
        if month in (11, 12):
            hooks.append("Holiday season — reduced staffing, increased phishing")
        if month in (6, 7, 8):
            hooks.append("Summer vacation period — skeleton crew risk")

        return hooks

    @staticmethod
    def derive_vendor_pretexts(meta: CompanyMetadata) -> List[str]:
        """Build vendor-impersonation scenarios from known vendors."""
        pretexts: List[str] = []
        for vendor in meta.known_vendors[:5]:
            pretexts.append(f"Security alert from {vendor} — urgent patch required")
        if not pretexts:
            pretexts.append("Cloud provider security notification")
        return pretexts


